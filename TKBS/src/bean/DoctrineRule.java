package bean;

public class DoctrineRule {

	private Integer doctrineRuleId;
	private String doctrineName;
	private String doctrineType;
	private String doctrineReferenceNo;
	private String doctrineDateCreated;
	private String doctrineChapterNo;
	private String doctrineSectionNo;
	private String doctrineSummary;
	private String keywords;
	
	public Integer getDoctrineRuleId() {
		return doctrineRuleId;
	}
	public void setDoctrineRuleId(Integer doctrineRuleId) {
		this.doctrineRuleId = doctrineRuleId;
	}
	public String getDoctrineName() {
		return doctrineName;
	}
	public void setDoctrineName(String doctrineName) {
		this.doctrineName = doctrineName;
	}
	public String getDoctrineType() {
		return doctrineType;
	}
	public void setDoctrineType(String doctrineType) {
		this.doctrineType = doctrineType;
	}
	public String getDoctrineReferenceNo() {
		return doctrineReferenceNo;
	}
	public void setDoctrineReferenceNo(String doctrineReferenceNo) {
		this.doctrineReferenceNo = doctrineReferenceNo;
	}
	public String getDoctrineDateCreated() {
		return doctrineDateCreated;
	}
	public void setDoctrineDateCreated(String doctrineDateCreated) {
		this.doctrineDateCreated = doctrineDateCreated;
	}
	public String getDoctrineChapterNo() {
		return doctrineChapterNo;
	}
	public void setDoctrineChapterNo(String doctrineChapterNo) {
		this.doctrineChapterNo = doctrineChapterNo;
	}
	public String getDoctrineSectionNo() {
		return doctrineSectionNo;
	}
	public void setDoctrineSectionNo(String doctrineSectionNo) {
		this.doctrineSectionNo = doctrineSectionNo;
	}
	public String getDoctrineSummary() {
		return doctrineSummary;
	}
	public void setDoctrineSummary(String doctrineSummary) {
		this.doctrineSummary = doctrineSummary;
	}
	public String getKeywords() {
		return keywords;
	}
	public void setKeywords(String keywords) {
		this.keywords = keywords;
	}
	
	
}
