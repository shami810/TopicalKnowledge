package bean;

public class DoctrineNotes {
	
	private Integer doctrineNotesId;
	private Integer doctrineReferenceNo;
	private String doctrineNotes;
	private String doctrineNotesDateCreated;
	public Integer getDoctrineNotesId() {
		return doctrineNotesId;
	}
	public void setDoctrineNotesId(Integer doctrineNotesId) {
		this.doctrineNotesId = doctrineNotesId;
	}
	public Integer getDoctrineReferenceNo() {
		return doctrineReferenceNo;
	}
	public void setDoctrineReferenceNo(Integer doctrineReferenceNo) {
		this.doctrineReferenceNo = doctrineReferenceNo;
	}
	public String getDoctrineNotes() {
		return doctrineNotes;
	}
	public void setDoctrineNotes(String doctrineNotes) {
		this.doctrineNotes = doctrineNotes;
	}
	public String getDoctrineNotesDateCreated() {
		return doctrineNotesDateCreated;
	}
	public void setDoctrineNotesDateCreated(String doctrineNotesDateCreated) {
		this.doctrineNotesDateCreated = doctrineNotesDateCreated;
	}
	
	
	
}
