package bean;

public class Keywords {
	
	private Integer keywoardsId;
	private String doctrineReferenceNo;
	private String keywoardName;
	public Integer getKeywoardsId() {
		return keywoardsId;
	}
	public void setKeywoardsId(Integer keywoardsId) {
		this.keywoardsId = keywoardsId;
	}
	public String getDoctrineReferenceNo() {
		return doctrineReferenceNo;
	}
	public void setDoctrineReferenceNo(String doctrineReferenceNo) {
		this.doctrineReferenceNo = doctrineReferenceNo;
	}
	public String getKeywoardName() {
		return keywoardName;
	}
	public void setKeywoardName(String keywoardName) {
		this.keywoardName = keywoardName;
	}
	
	
}
