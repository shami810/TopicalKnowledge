package bean;

public class DoctrineCases {

	private Integer controllingCasesId;
	private Integer doctrineReferenceNo;
	private String sectionNo;
	private String chapterNo;
	private String caseName;
	private String caseCode;
	private String caseYear;
	private String caseSummary;
	
	public String getCaseSummary() {
		return caseSummary;
	}
	public void setCaseSummary(String caseSummary) {
		this.caseSummary = caseSummary;
	}
	public Integer getControllingCasesId() {
		return controllingCasesId;
	}
	public void setControllingCasesId(Integer controllingCasesId) {
		this.controllingCasesId = controllingCasesId;
	}
	public Integer getDoctrineReferenceNo() {
		return doctrineReferenceNo;
	}
	public void setDoctrineReferenceNo(Integer doctrineReferenceNo) {
		this.doctrineReferenceNo = doctrineReferenceNo;
	}
	public String getSectionNo() {
		return sectionNo;
	}
	public void setSectionNo(String sectionNo) {
		this.sectionNo = sectionNo;
	}
	public String getChapterNo() {
		return chapterNo;
	}
	public void setChapterNo(String chapterNo) {
		this.chapterNo = chapterNo;
	}
	public String getCaseName() {
		return caseName;
	}
	public void setCaseName(String caseName) {
		this.caseName = caseName;
	}
	public String getCaseCode() {
		return caseCode;
	}
	public void setCaseCode(String caseCode) {
		this.caseCode = caseCode;
	}
	public String getCaseYear() {
		return caseYear;
	}
	public void setCaseYear(String caseYear) {
		this.caseYear = caseYear;
	}
	
	
	
}
